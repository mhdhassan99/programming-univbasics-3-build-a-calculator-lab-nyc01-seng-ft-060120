def addition(num1, num2)
  result = num2 + num1
  return result
end

def subtraction(num1, num2)
  result = (num2 - num1)
  return result.abs
end

def division(num1, num2)
  result = (num1 / num2)
  return result.abs
end

def multiplication(num1, num2)
  result = num1 * num2
  return result
end

def modulo(num1, num2)
  result = num1 % num2
  return result.abs
end

def square_root(num)
  return Math.sqrt(num)
end